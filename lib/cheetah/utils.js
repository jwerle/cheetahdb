/**
 * @see https://github.com/aheckmann/sliced
 * An Array.prototype.slice.call(arguments) alternative
 *
 * @param {Object} args something with a length
 * @param {Number} slice
 * @param {Number} sliceEnd
 * @api public
 */

var sliced = module.exports.sliced = function (args, slice, sliceEnd) {
  var ret = [];
  var len = args.length;

  if (0 === len) return ret;

  var start = slice < 0
    ? Math.max(0, slice + len)
    : slice || 0;

  if (sliceEnd !== undefined) {
    len = sliceEnd < 0
      ? sliceEnd + len
      : sliceEnd
  }

  while (len-- > start) {
    ret[len - start] = args[len];
  }

  return ret;
};


/**
  * @public
  * @function utils.isUUID
  *
  *   Performs a test on a given string to test whether the string is a valid uuid
  *
  * @param {String} string - The string to test
  * @return {Boolean} - A boolean to indicate whether the string is a valid uuid
  */
utils.isUUID = function(string) {
  var uuidStructure = utils.guid()
  if (typeof string !== 'string') return false; // check that shit right away  
  if (string.length !== uuidStructure.length) return false;
  if ((/[g-z]/g).test(string)) return false;
  if (string.split('-').length !== 5) return false;
  return true;
};

utils.s4 = function s4() {
  return Math.floor((1 + Math.random()) * 0x10000).toString(16).substring(1);
};

utils.guid = function guid() {
  var s4 = utils.s4
  return s4() + s4() + '-' + s4() + '-' + s4() + '-' + s4() + '-' + s4() + s4() + s4();
}

/**
  * @public
  * @function utils.merge
  *
  *   Merges two or more objects together. Also performs deep merging
  *   Ex:
  *     o = {foo: 'bar', i: {p :'hi', foo:{bar:'bar'}}}
  *     n = utils.merge(o, {bar:'food', i: {p:'heyy', u:'u'}}, {foo:'yay'})
  *     console.log(n)
  *
  * @param {Object} object - An object to who is merged into
  * @param {Object} objectN - An object to merge into the first object
  * @return {Object} - Returns the newly merged object
  */
exports.merge = function(/*, objectN*/) {
  var key, value, objects, rootObject, sourceObject

  objects = exports.toArray(arguments); // convert to array
  rootObject = objects.shift(); // shift off merge reciever
  if (objects.length >= 2) {
    objects = objects.map(function(object, i, objects){
      var nextUp = objects.slice(i + 1);
      rootObject = exports.merge(rootObject, object);
    });
  }
  else if (objects.length === 1) { // must be 1 for a merge
    sourceObject = objects.shift();
    for (key in sourceObject) {
      value = sourceObject[key];
      rootObject[key] = (typeof value === 'object' && typeof rootObject[key] === 'object')
                          ? exports.merge(rootObject[key], value) 
                          : value;
    }
  }

  return rootObject;
};

